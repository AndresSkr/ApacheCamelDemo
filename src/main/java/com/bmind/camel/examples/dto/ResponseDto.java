package com.bmind.camel.examples.dto;

public class ResponseDto {
	
	private int response;
	
	public ResponseDto() {
		super();
	}

	public ResponseDto(int response) {
		super();
		this.response = response;
	}

	public int getResponse() {
		return response;
	}

	public void setResponse(int response) {
		this.response = response;
	}
	
	

}
